package ua.olifirenko.hw2;

public class TaskCW3_Increment {
    public static void main(String[] args) {
        int number = 15;
        System.out.println(number);
        number = number + 1;
        System.out.println(number);
        number +=1;
        System.out.println(number++);
        System.out.println(number);
        System.out.println(++number);
    }
}
